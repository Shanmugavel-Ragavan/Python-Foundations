# Single and Multiline Comment in Python

# In Python, there are two types of comments: single-line and multi-line.

# 1. Single-line comments start with a hash symbol (#) and extend to the end of the line:
# 2. Multi-line comments start and end with three quotation marks (""").

# This is a single-line comment
a = 10  # Assign 10 to variable a
b = 20  # Assign 20 to variable b
c = a + b  # Add a and b
print(c)  # Output the result

# Using Both Comments

# Single-line comment explaining the next line
x = 5
y = 15

# multi-line comment
"""
This is a multi-line comment explaining
the addition operation below.
It can span multiple lines.
"""
z = x + y  # Sum of x and y
print("Sum:", z)